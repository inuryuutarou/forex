                        <h3>Introducing Broker (IB)</h3>
                        <p>Komisi hingga $10 per lot + Transfers ke/dari akun klien<br>
						IB adalah partner yang menjaga portofolio bisnis dan menerima komisi bulanan untuk semua klien dan sub-IB yang mereka ajukan ke XM.<br>
						<b><i>Dapatkan hingga $10 per lot pada klien yang diajukan dan 10% pada pengajuan IB lainnya. Semua IB juga memiliki akses penuh ke area partner yang menyediakan statistik dari performa mereka.</i></b></p>
